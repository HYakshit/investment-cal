                    <div class="main-title">

                        <b class="text-center">Do’s</b>

                    </div>

                    <ul class="list-unstyled">

                        <li><b>Follow Post-Op Instructions</b> - Adhere strictly to the surgeon's guidelines regarding
                            medication, cleaning, and activity restrictions to facilitate proper healing.</li>

                        <li><b>Gentle Hair Care</b> - Handle newly transplanted hair with care. Avoid excessive
                            touching, scratching, or brushing to prevent damage or dislodging of grafts.</li>

                        <li><b>Avoid Sun Exposure</b> - Protect the treated area from direct sunlight to prevent sunburn
                            and irritation. Wear a hat or use sunscreen as recommended by your surgeon.</li>

                        <li><b>Limit Physical Activity</b> - Avoid strenuous exercise and activities that increase blood
                            flow to the scalp during the initial recovery period to prevent complications and ensure
                            graft survival.</li>

                        <li><b>Regular Follow-ups</b> - Attend scheduled follow-up appointments with your surgeon for
                            evaluation and guidance. Communicate any concerns promptly to ensure the best possible
                            outcome.</li>

                    </ul>

                </div>

            </div>

            <div class="col-lg-6 do-dont-card">

                <div class="l-part">

                    <div class="main-title">

                        <b class="text-center">Don’ts</b>

                    </div>

                    <ul class="list-unstyled">

                        <li><b>No Smoking or Alcohol</b> - Avoid smoking and alcohol as they can hinder the healing
                            process and affect blood circulation to the scalp.</li>

                        <li><b>Don't Scratch or Pick</b> - Refrain from scratching or picking at the transplanted area
                            to prevent damage to grafts and promote proper healing.</li>

                        <li><b>Avoid Tight Headwear</b> - Steer clear of tight headwear that can put pressure on the
                            transplant site, potentially dislodging grafts or causing discomfort.</li>

                        <li><b>Say No to Harsh Products</b> - Avoid using harsh hair care products, chemicals, or
                            styling tools that may irritate the scalp or compromise the healing process.</li>

                        <li><b>No Swimming or Saunas</b> - Stay away from swimming pools and saunas until approved by
                            your surgeon, as exposure to chlorinated water or extreme heat can be detrimental to the
                            healing process.</li>

                    </ul>

